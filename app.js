function changeHierarchieHight() {
    button1.style.opacity = '100%'
    button2.style.opacity = '30%'
    button3.style.opacity = '30%'
    button4.style.opacity = '30%'
    d3.selectAll('.piece').style('display', (d, i) => {
        return i % 3 === 0 ? 'block' : 'none'
    })
}

